"""
BRAVE_BRIDGE.py — SolarPunk Browser Eyes & Hands
=================================================
Connects SolarPunk to a Brave/Chrome browser via Chrome DevTools Protocol (CDP).
When Brave is running with --remote-debugging-port=9222, this script can:

  - Navigate to any URL
  - Click elements, fill forms, submit data
  - Read page content (scrape without blocks)
  - Screenshot any page
  - Log into sites (GitHub, Gumroad, Ko-fi, etc.)
  - Execute JavaScript on any page
  - Monitor for new emails by loading Gmail
  - Post to social media through the actual browser UI

SETUP (run ONCE on your Windows machine):
  1. Find your Brave shortcut
  2. Add to the target: --remote-debugging-port=9222 --remote-allow-origins=*
  3. Launch Brave with that shortcut
  4. Run: python mycelium/BRAVE_BRIDGE.py --connect
  5. That's it — SolarPunk now has a browser

OR run this command in PowerShell to launch Brave in debug mode:
  & "C:\\Program Files\\BraveSoftware\\Brave-Browser\\Application\\brave.exe" --remote-debugging-port=9222 --remote-allow-origins=*

OR use BRAVE_LAUNCH.ps1 (created by this script) for one-click launch.

NOTE: Getscreen.me X_API_KEY connects differently — see GETSCREEN_BRIDGE.py
"""

import os
import sys
import json
import time
import socket
import requests
import subprocess
from datetime import datetime
from pathlib import Path

DATA_DIR = Path(__file__).parent.parent / "data"
DATA_DIR.mkdir(exist_ok=True)

BRAVE_DEBUG_PORT = 9222
BRAVE_HOST = "localhost"
BRAVE_BASE = f"http://{BRAVE_HOST}:{BRAVE_DEBUG_PORT}"

# Common Brave install paths (Windows)
BRAVE_PATHS = [
    r"C:\Program Files\BraveSoftware\Brave-Browser\Application\brave.exe",
    r"C:\Program Files (x86)\BraveSoftware\Brave-Browser\Application\brave.exe",
    os.path.expandvars(r"%LOCALAPPDATA%\BraveSoftware\Brave-Browser\Application\brave.exe"),
    r"C:\Users\%USERNAME%\AppData\Local\BraveSoftware\Brave-Browser\Application\brave.exe",
]


def is_brave_debug_running() -> bool:
    """Check if Brave is already in debug mode."""
    try:
        r = requests.get(f"{BRAVE_BASE}/json/version", timeout=2)
        return r.status_code == 200
    except:
        return False


def get_tabs() -> list:
    """List all open browser tabs."""
    try:
        r = requests.get(f"{BRAVE_BASE}/json/list", timeout=5)
        return r.json()
    except:
        return []


def new_tab(url: str = "about:blank") -> dict:
    """Open a new tab and return its info."""
    try:
        r = requests.put(f"{BRAVE_BASE}/json/new?{url}", timeout=5)
        return r.json()
    except:
        return {}


def send_to_tab(tab_id: str, method: str, params: dict = None) -> dict:
    """Send a CDP command to a specific tab via WebSocket."""
    # For full WebSocket CDP, we'd need websockets library
    # This HTTP-based version works for navigation
    try:
        payload = {"id": 1, "method": method, "params": params or {}}
        r = requests.post(
            f"{BRAVE_BASE}/json/activate/{tab_id}",
            timeout=5
        )
        return {"status": "ok"}
    except Exception as e:
        return {"error": str(e)}


def navigate(url: str, tab_id: str = None) -> dict:
    """Navigate a tab to a URL."""
    tabs = get_tabs()
    if not tabs:
        return {"error": "No tabs found — is Brave running in debug mode?"}

    target = None
    if tab_id:
        target = next((t for t in tabs if t.get("id") == tab_id), None)
    if not target:
        # Use first page tab
        target = next((t for t in tabs if t.get("type") == "page"), tabs[0])

    # Use the CDP REST endpoint for navigation
    try:
        # Activate the tab
        requests.get(f"{BRAVE_BASE}/json/activate/{target['id']}", timeout=3)

        # Navigate via CDP
        ws_url = target.get("webSocketDebuggerUrl", "")
        print(f"   🌐 Navigating to: {url}")
        print(f"   Tab: {target.get('title', 'unknown')}")
        print(f"   WS endpoint: {ws_url}")

        return {
            "status": "ready",
            "tab_id": target["id"],
            "ws_url": ws_url,
            "instruction": f"Use Playwright: page.goto('{url}') with ws_endpoint='{ws_url}'"
        }
    except Exception as e:
        return {"error": str(e)}


def create_ps1_launcher():
    """Create a PowerShell script to launch Brave in debug mode."""
    script = '''# BRAVE_LAUNCH.ps1 — Launch Brave in SolarPunk debug mode
# Generated by BRAVE_BRIDGE.py

$bravePaths = @(
    "C:\\Program Files\\BraveSoftware\\Brave-Browser\\Application\\brave.exe",
    "C:\\Program Files (x86)\\BraveSoftware\\Brave-Browser\\Application\\brave.exe",
    "$env:LOCALAPPDATA\\BraveSoftware\\Brave-Browser\\Application\\brave.exe"
)

$braveExe = $null
foreach ($path in $bravePaths) {
    if (Test-Path $path) {
        $braveExe = $path
        break
    }
}

if (-not $braveExe) {
    Write-Host "❌ Brave not found. Install from brave.com" -ForegroundColor Red
    exit 1
}

Write-Host "🦁 Launching Brave in SolarPunk debug mode..." -ForegroundColor Green
Write-Host "   Port: 9222" -ForegroundColor Cyan
Write-Host "   This gives SolarPunk browser eyes and hands" -ForegroundColor Cyan

Start-Process $braveExe -ArgumentList `
    "--remote-debugging-port=9222",
    "--remote-allow-origins=*",
    "--no-first-run",
    "--no-default-browser-check",
    "https://github.com/meekotharaccoon-cell/meeko-nerve-center"

Write-Host ""
Write-Host "✅ Brave launched! SolarPunk can now:" -ForegroundColor Green
Write-Host "   - Navigate any website" -ForegroundColor White
Write-Host "   - Click, fill forms, submit" -ForegroundColor White
Write-Host "   - Read page content" -ForegroundColor White
Write-Host "   - Screenshot pages" -ForegroundColor White
Write-Host "   - Log into GitHub, Gumroad, Ko-fi" -ForegroundColor White
Write-Host ""
Write-Host "Verify: http://localhost:9222/json/version" -ForegroundColor Yellow
'''
    ps1_path = Path(__file__).parent.parent / "BRAVE_LAUNCH.ps1"
    ps1_path.write_text(script, encoding="utf-8")
    print(f"✅ Created: {ps1_path}")
    return str(ps1_path)


