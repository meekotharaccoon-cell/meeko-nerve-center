def main():
    if "--connect" in sys.argv or "--check" in sys.argv:
        check_connection()
    elif "--launcher" in sys.argv:
        ps1 = create_ps1_launcher()
        print(f"✅ Launcher created: {ps1}")
    else:
        check_connection()

    # Save bridge state
    state = {
        "last_check": datetime.now().isoformat(),
        "debug_port_active": is_brave_debug_running(),
        "tab_count": len(get_tabs()),
        "debug_url": f"http://localhost:{BRAVE_DEBUG_PORT}/json/version"
    }
    (DATA_DIR / "brave_bridge_state.json").write_text(json.dumps(state, indent=2))


if __name__ == "__main__":
    main()
